#code to calculate average age of 6 boys
a = float(input("enter the age of the first boy : "))
b = float(input("enter the age of the second boy : "))
c = float(input("enter the age of the third boy : "))
d = float(input("enter the age of the fourth boy : "))
e = float(input("enter the age of the fifth boy : "))
f = float(input("enter the age of the sixth boy : "))
average_age = (a + b + c + d + e + f) / 6
print(average_age)